#!/usr/bin/env bash
# Build the NixOS qcow2 (all five proxies + tuning), push it to Yandex Object
# Storage under a CONTENT-HASHED key, and write the URL to
# terraform/image.auto.tfvars.
#
# Because the object key moves whenever the built image changes, a plain
# `make up` then sees a new source_url, recreates the YC image + instances, and
# rewrites inventory.json — no manual `-replace`, no stale inventory.
#
# Requires: nix, awscli2, and:
#   BENCH_BUCKET             your YC Object Storage bucket name
#   AWS_ACCESS_KEY_ID / AWS_SECRET_ACCESS_KEY   a YC static access key (S3 API)
set -euo pipefail
cd "$(dirname "$0")/.."

BUCKET=${BENCH_BUCKET:?set BENCH_BUCKET to your Yandex Object Storage bucket}
ENDPOINT=https://storage.yandexcloud.net

echo "== nix build .#image =="
nix build .#packages.x86_64-linux.image -o result-image

img=$(find -L result-image -name '*.qcow2' | head -1)
[ -n "$img" ] || { echo "no qcow2 under result-image/" >&2; exit 1; }

# Content hash from the image's nix store path — changes iff the image changes.
hash=$(basename "$(readlink -f result-image)" | cut -d- -f1 | cut -c1-16)
KEY=${BENCH_IMAGE_KEY:-zoxy-benchmark/nixos-${hash}.qcow2}
URL="$ENDPOINT/$BUCKET/$KEY"
echo "built: $img ($(du -h "$img" | cut -f1)), hash=$hash"

echo "== upload s3://$BUCKET/$KEY =="
aws --endpoint-url "$ENDPOINT" s3 cp "$img" "s3://$BUCKET/$KEY"

# terraform auto-loads *.auto.tfvars and it overrides terraform.tfvars, so this
# hashed URL wins and drives the recreate on the next `make up`.
cat > terraform/image.auto.tfvars <<EOF
# AUTO-GENERATED by scripts/build-image.sh — do not edit; gitignored.
image_source_url = "$URL"
EOF

echo
echo "wrote terraform/image.auto.tfvars  ->  image_source_url = $URL"
echo "next: make up   (recreates image + fleet from the new qcow, refreshes inventory)"
