#!/usr/bin/env bash
# Cloud orchestration, all from the laptop:
#   1. read VM IPs from terraform output
#   2. render the cloud env + prometheus targets
#   3. rsync the repo to the three VMs (no images, no bake — edit & rerun)
#   4. bring up backend + monitoring, then hand off to run-all.sh (MODE=cloud)
#
# Prereqs: cd cloud && tofu init && tofu apply   (make cloud-up)
set -euo pipefail
cd "$(dirname "$0")/.."

TF=${TF:-tofu}
SSH_USER=${SSH_USER:-ubuntu}
REMOTE_DIR=${REMOTE_DIR:-bench}
if [[ -f .env ]]; then set -a; source .env; set +a; fi

inv=$($TF -chdir=cloud output -json inventory)
ip() { echo "$inv" | jq -r ".$1.$2"; }
LOADGEN_PUB=$(ip loadgen external_ip)
PROXY_PUB=$(ip proxy external_ip)
BACKEND_PUB=$(ip backend external_ip)
LOADGEN_PRIV=$(ip loadgen internal_ip)
PROXY_PRIV=$(ip proxy internal_ip)
BACKEND_PRIV=$(ip backend internal_ip)

PROXY_CPUS=${PROXY_CPUS:-2}
PROXY_CPUSET="0-$((PROXY_CPUS - 1))"

echo ">>> fleet: loadgen=$LOADGEN_PUB proxy=$PROXY_PUB backend=$BACKEND_PUB"

# --- prometheus targets for the cloud fleet (file_sd) -------------------------
mkdir -p monitoring/targets/cloud
for f in cadvisor:8081 node:9100; do
    name=${f%%:*} port=${f##*:}
    cat > "monitoring/targets/cloud/$name.yml" <<EOF
# generated by cloud-run.sh — do not edit
- targets: ["$LOADGEN_PRIV:$port"]
  labels: { role: loadgen }
- targets: ["$PROXY_PRIV:$port"]
  labels: { role: proxy }
- targets: ["$BACKEND_PRIV:$port"]
  labels: { role: backend }
EOF
done

# --- cloud .env: user's .env + the deployment facts ---------------------------
grep -vE '^(PROM_TARGETS|BACKEND_IP|PROXY_IP|PROXY_CPUSET|PROM_URL)=' .env 2>/dev/null > .env.cloud || true
cat >> .env.cloud <<EOF
PROM_TARGETS=cloud
BACKEND_IP=$BACKEND_PRIV
PROXY_IP=$PROXY_PRIV
PROXY_CPUSET=$PROXY_CPUSET
PROM_URL=http://$LOADGEN_PUB:9090
EOF

# --- ship the repo -------------------------------------------------------------
for host in "$LOADGEN_PUB" "$PROXY_PUB" "$BACKEND_PUB"; do
    echo ">>> rsync -> $host"
    rsync -az --delete \
        --exclude .git --exclude results --exclude .env --exclude .env.cloud \
        --exclude 'cloud/.terraform*' --exclude 'cloud/terraform.tfstate*' \
        ./ "$SSH_USER@$host:$REMOTE_DIR/"
    rsync -az .env.cloud "$SSH_USER@$host:$REMOTE_DIR/.env"
done

# --- static roles up -------------------------------------------------------------
COMPOSE="docker compose -f compose.yaml -f compose.cloud.yaml"
ssh -o BatchMode=yes "$SSH_USER@$BACKEND_PUB" "cd $REMOTE_DIR && $COMPOSE --profile backend up -d --wait"
ssh -o BatchMode=yes "$SSH_USER@$LOADGEN_PUB" "cd $REMOTE_DIR && $COMPOSE --profile monitoring up -d --wait && mkdir -p results"

echo ">>> grafana: http://$LOADGEN_PUB:3000  prometheus: http://$LOADGEN_PUB:9090"

# --- hand off to the shared driver ------------------------------------------------
MODE=cloud \
    PROXY_SSH="$SSH_USER@$PROXY_PUB" \
    LOADGEN_SSH="$SSH_USER@$LOADGEN_PUB" \
    PROXY_IP="$PROXY_PRIV" \
    BACKEND_IP="$BACKEND_PRIV" \
    REMOTE_DIR="$REMOTE_DIR" \
    ./scripts/run-all.sh

echo ">>> render the report against the cloud prometheus:"
echo "    PROM_URL=http://$LOADGEN_PUB:9090 make report"
