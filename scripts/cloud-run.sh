#!/usr/bin/env bash
# Cloud orchestration, all from the laptop:
#   1. read VM IPs from terraform output
#   2. render the cloud env + prometheus targets
#   3. rsync the repo to the three VMs (no images, no bake — edit & rerun)
#   4. bring up backend + monitoring, then hand off to run-all.sh (MODE=cloud)
#
# Prereqs: cd cloud && tofu init && tofu apply   (make cloud-up)
set -euo pipefail
cd "$(dirname "$0")/.."

TF=${TF:-tofu}
SSH_USER=${SSH_USER:-ubuntu}
REMOTE_DIR=${REMOTE_DIR:-bench}
# .env provides defaults but must NOT clobber explicit overrides — the same
# save/restore run-all.sh does (e.g. `PROXIES="zoxy haproxy" make cloud-bench`)
KNOBS=(PROXIES MAX_RATE RAMP_DURATION WARM_RATE MAX_VUS REQ_PATH COOLDOWN PROXY_CPUS PROXY_MEM ZOXY_REF ZOXY_OPTIMIZE RELAY_BUFFERS_MAX)
for k in "${KNOBS[@]}"; do [[ -n ${!k+x} ]] && eval "_saved_$k=\${$k}"; done
if [[ -f .env ]]; then set -a; source .env; set +a; fi
for k in "${KNOBS[@]}"; do
    saved="_saved_$k"
    [[ -n ${!saved+x} ]] && eval "export $k=\${$saved}"
done

inv=$($TF -chdir=cloud output -json inventory)
# `// empty` so an absent host (e.g. a single-loadgen fleet with no loadgen2)
# yields "" rather than the literal "null"; every loadgen2 use is guarded below.
ip() { echo "$inv" | jq -r ".$1.$2 // empty"; }
LOADGEN_PUB=$(ip loadgen external_ip)
LOADGEN2_PUB=$(ip loadgen2 external_ip)
PROXY_PUB=$(ip proxy external_ip)
BACKEND_PUB=$(ip backend external_ip)
LOADGEN_PRIV=$(ip loadgen internal_ip)
LOADGEN2_PRIV=$(ip loadgen2 internal_ip)
PROXY_PRIV=$(ip proxy internal_ip)
BACKEND_PRIV=$(ip backend internal_ip)

PROXY_CPUS=${PROXY_CPUS:-1}
PROXY_CPUSET="0-$((PROXY_CPUS - 1))"

echo ">>> fleet: loadgen=$LOADGEN_PUB loadgen2=${LOADGEN2_PUB:-<none>} proxy=$PROXY_PUB backend=$BACKEND_PUB"

# --- prometheus targets for the cloud fleet (file_sd) -------------------------
mkdir -p monitoring/targets/cloud
for f in cadvisor:8081 node:9100; do
    name=${f%%:*} port=${f##*:}
    cat > "monitoring/targets/cloud/$name.yml" <<EOF
# generated by cloud-run.sh — do not edit
- targets: ["$LOADGEN_PRIV:$port"]
  labels: { role: loadgen }
- targets: ["$PROXY_PRIV:$port"]
  labels: { role: proxy }
- targets: ["$BACKEND_PRIV:$port"]
  labels: { role: backend }
EOF
    # second loadgen is optional (single-loadgen fleets omit it)
    [[ -n $LOADGEN2_PRIV ]] && cat >> "monitoring/targets/cloud/$name.yml" <<EOF
- targets: ["$LOADGEN2_PRIV:$port"]
  labels: { role: loadgen }
EOF
done

# --- cloud .env: user's .env + explicit knob overrides + deployment facts -----
# The loadgen VM's k6 and the proxy VM's compose read THIS file (rsynced to
# bench/.env), NOT the driver's shell — so an explicit override like
# `MAX_RATE=250000 make cloud-bench` must be written in here or it silently
# runs the file's default remotely while the driver banner shows the override
# (a split-brain that produced a mislabeled run once). Strip every knob we may
# re-inject, then append the ones currently set to a clean, comment-free value
# so there's exactly one definition and no last-wins ambiguity.
strip_re="^(PROM_TARGETS|BACKEND_IP|PROXY_IP|PROXY_CPUSET|PROM_URL"
for k in "${KNOBS[@]}"; do strip_re+="|$k"; done
strip_re+=")="
grep -vE "$strip_re" .env 2>/dev/null > .env.cloud || true
for k in "${KNOBS[@]}"; do
    [[ -n ${!k+x} ]] && printf '%s=%s\n' "$k" "${!k}" >> .env.cloud
done
cat >> .env.cloud <<EOF
PROM_TARGETS=cloud
BACKEND_IP=$BACKEND_PRIV
PROXY_IP=$PROXY_PRIV
PROXY_CPUSET=$PROXY_CPUSET
PROM_URL=http://$LOADGEN_PUB:9090
EOF

# --- ship the repo -------------------------------------------------------------
for host in "$LOADGEN_PUB" ${LOADGEN2_PUB:+"$LOADGEN2_PUB"} "$PROXY_PUB" "$BACKEND_PUB"; do
    echo ">>> rsync -> $host"
    rsync -az --delete \
        --exclude .git --exclude results --exclude .env --exclude .env.cloud \
        --exclude 'cloud/.terraform*' --exclude 'cloud/terraform.tfstate*' \
        ./ "$SSH_USER@$host:$REMOTE_DIR/"
    rsync -az .env.cloud "$SSH_USER@$host:$REMOTE_DIR/.env"
done

# --- static roles up -------------------------------------------------------------
COMPOSE="docker compose -f compose.yaml -f compose.cloud.yaml"
ssh -o BatchMode=yes "$SSH_USER@$BACKEND_PUB" "cd $REMOTE_DIR && $COMPOSE --profile backend up -d --wait"
ssh -o BatchMode=yes "$SSH_USER@$LOADGEN_PUB" "cd $REMOTE_DIR && $COMPOSE --profile monitoring up -d --wait && mkdir -p results"
# optional second loadgen runs k6 only — but bring up its exporters so its host
# CPU is scraped, and create results/ for k6's handleSummary write.
[[ -n $LOADGEN2_PUB ]] && ssh -o BatchMode=yes "$SSH_USER@$LOADGEN2_PUB" "cd $REMOTE_DIR && $COMPOSE --profile monitoring up -d --wait cadvisor node_exporter && mkdir -p results"

echo ">>> grafana: http://$LOADGEN_PUB:3000  prometheus: http://$LOADGEN_PUB:9090"

# --- hand off to the shared driver ------------------------------------------------
# LOADGEN2_SSH set => run-all.sh fans k6 across both loadgens (split rate, VU
# pool each = MAX_VUS, distinct lg tag). LOADGEN_PRIV is where loadgen2's k6
# points its remote-write (prometheus lives on the primary loadgen).
# DRIVER selects the measurement: run-all.sh (the single ramp, default) or
# sweep.sh (the closed-loop concurrency sweep). Both read the same handoff env.
MODE=cloud \
    PROXY_SSH="$SSH_USER@$PROXY_PUB" \
    LOADGEN_SSH="$SSH_USER@$LOADGEN_PUB" \
    LOADGEN2_SSH="${LOADGEN2_PUB:+$SSH_USER@$LOADGEN2_PUB}" \
    LOADGEN_PRIV="$LOADGEN_PRIV" \
    PROXY_IP="$PROXY_PRIV" \
    BACKEND_IP="$BACKEND_PRIV" \
    REMOTE_DIR="$REMOTE_DIR" \
    "./scripts/${DRIVER:-run-all.sh}"

echo ">>> render the report against the cloud prometheus:"
echo "    PROM_URL=http://$LOADGEN_PUB:9090 make report"
