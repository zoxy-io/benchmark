#!/usr/bin/env bash
# Single-loadgen, OPEN-LOOP suite driver: ramps each proxy with vegeta-ramp
# (loadgen/vegeta-ramp) and writes per-proxy CSVs + meta.json for
# report/report_vegeta.py. The open-loop analogue of run-all.sh — one 16-core
# loadgen saturates any proxy at ~25% CPU, so no second loadgen / no k6.
#
#   PROXIES="direct zoxy haproxy" MAX_RATE=160000 make ...  (or run directly)
set -euo pipefail
cd "$(dirname "$0")/.."

TF=${TF:-tofu}; SSH_USER=${SSH_USER:-ubuntu}; REMOTE=${REMOTE_DIR:-bench}
PROXIES=${PROXIES:-"direct zoxy haproxy envoy traefik nginx pingora"}
MAX_RATE=${MAX_RATE:-160000}
RAMP_SECONDS=${RAMP_SECONDS:-120}
START_RATE=${START_RATE:-200}
MAX_WORKERS=${MAX_WORKERS:-2000}   # in-flight concurrency cap (open-loop guard)
CONNECTIONS=${CONNECTIONS:-2000}
PROXY_CPUS=${PROXY_CPUS:-4}
ZOXY_REF=${ZOXY_REF:-main}
COOLDOWN=${COOLDOWN:-8}
RUNID=${RUNID:-vegeta-$(date -u +%Y%m%d-%H%M%S)}

inv=$($TF -chdir=cloud output -json inventory)
ip() { echo "$inv" | jq -r ".$1.$2 // empty"; }
LG=$(ip loadgen external_ip); PROXY=$(ip proxy external_ip)
LG_PRIV=$(ip loadgen internal_ip)
PROXY_PRIV=$(ip proxy internal_ip); BACKEND_PRIV=$(ip backend internal_ip)
PROM="http://$LG:9090"; CPUSET="0-$((PROXY_CPUS - 1))"
RESULTS="results/$RUNID"; mkdir -p "$RESULTS"
ln -sfn "$RUNID" results/latest   # `make report` renders results/latest
COMPOSE="docker compose -f compose.yaml -f compose.cloud.yaml"
PENV="ZOXY_REF=$ZOXY_REF PROXY_CPUS=$PROXY_CPUS PROXY_CPUSET=$CPUSET BACKEND_IP=$BACKEND_PRIV"

echo ">>> runid=$RUNID proxies=[$PROXIES] ramp=$START_RATE->${MAX_RATE}rps/${RAMP_SECONDS}s workers<=$MAX_WORKERS cpus=$PROXY_CPUS"

# --- ship repo to proxy+backend; ensure backend, exporters, vegeta-ramp up ------
BACKEND_PUB=$(ip backend external_ip)
for h in "$PROXY" "$BACKEND_PUB"; do
    rsync -az --delete --exclude .git --exclude results --exclude .env --exclude .env.cloud \
        --exclude 'cloud/.terraform*' --exclude 'cloud/terraform.tfstate*' ./ "$SSH_USER@$h:$REMOTE/"
done
ssh -o BatchMode=yes "$SSH_USER@$BACKEND_PUB" "cd $REMOTE && $COMPOSE --profile backend up -d --wait" >/dev/null 2>&1 || true
ssh -o BatchMode=yes "$SSH_USER@$PROXY" "cd $REMOTE && $PENV $COMPOSE --profile monitoring up -d cadvisor node_exporter" >/dev/null 2>&1 || true
# point prometheus's live-metrics scrape at this loadgen; ship the monitoring
# config (prometheus job + vegeta dashboard) so Grafana shows the ramp live.
# file_sd auto-reloads the target; the prometheus.yml `vegeta` job is a one-time
# add (restart prometheus once after first deploy).
mkdir -p monitoring/targets/cloud
cat > monitoring/targets/cloud/vegeta.yml <<EOF
# regenerated by vegeta-bench.sh
- targets: ["$LG_PRIV:8090"]
  labels: { role: loadgen }
EOF
rsync -az monitoring/ "$SSH_USER@$LG:$REMOTE/monitoring/"
if ! ssh -o BatchMode=yes "$SSH_USER@$LG" 'test -x ~/vegeta-ramp/vegeta-ramp'; then
    echo ">>> building vegeta-ramp on the loadgen"
    ssh -o BatchMode=yes "$SSH_USER@$LG" 'mkdir -p ~/vegeta-ramp'
    rsync -az loadgen/vegeta-ramp/ "$SSH_USER@$LG:vegeta-ramp/"
    ssh -o BatchMode=yes "$SSH_USER@$LG" 'cd ~/vegeta-ramp && docker run --rm -v "$PWD":/src -w /src golang:1.23 sh -c "CGO_ENABLED=0 go build -o vegeta-ramp ."'
fi

meta="$RESULTS/meta.json"
echo "{\"prom\":\"$PROM\",\"runid\":\"$RUNID\",\"runs\":{}}" > "$meta"

record() { # proxy start end
    python3 - "$meta" "$1" "$2" "$3" "$MAX_RATE" "$RAMP_SECONDS" "$START_RATE" <<'PY'
import json,sys
f,p,s,e,mr,rs,sr=sys.argv[1:8]
m=json.load(open(f))
m["runs"][p]={"start":s,"end":e,"max_rate":int(mr),"ramp_seconds":int(rs),
              "start_rate":int(sr),"loadgens":["lg1"]}
json.dump(m,open(f,"w"),indent=2)
PY
}

for p in $PROXIES; do
    echo ">>> [$p] starting"
    if [[ $p == direct ]]; then
        target="http://$BACKEND_PRIV:9000/1k"
    else
        ssh -o BatchMode=yes "$SSH_USER@$PROXY" "cd $REMOTE && $PENV $COMPOSE --profile $p up -d --build --wait $p" >/dev/null
        target="http://$PROXY_PRIV:8080/1k"
    fi
    # warm probe
    for i in $(seq 1 20); do
        ssh -o BatchMode=yes "$SSH_USER@$LG" "curl -sf -o /dev/null $target" && break
        [[ $i == 20 ]] && { echo "fatal: [$p] never served 200 at $target"; exit 1; }
        sleep 1
    done

    echo ">>> [$p] ramping ${RAMP_SECONDS}s"
    start=$(date -u +%Y-%m-%dT%H:%M:%SZ)
    ssh -o BatchMode=yes "$SSH_USER@$LG" "docker run --rm --network host --ulimit nofile=1048576 \
        -v ~/vegeta-ramp:/w -w /w \
        -e TARGET=$target -e MAX_RATE=$MAX_RATE -e RAMP_SECONDS=$RAMP_SECONDS -e START_RATE=$START_RATE \
        -e CONNECTIONS=$CONNECTIONS -e MAX_WORKERS=$MAX_WORKERS -e OUT=/w/$p.lg1.csv -e NAME=$p -e RUNID=$RUNID \
        alpine:3 /w/vegeta-ramp" 2>&1 | grep -E 'peak|knee' || true
    end=$(date -u +%Y-%m-%dT%H:%M:%SZ)
    scp -q "$SSH_USER@$LG:vegeta-ramp/$p.lg1.csv" "$RESULTS/"
    record "$p" "$start" "$end"

    if [[ $p != direct ]]; then
        ssh -o BatchMode=yes "$SSH_USER@$PROXY" "cd $REMOTE && $COMPOSE --profile $p stop $p && $COMPOSE --profile $p rm -f $p" >/dev/null 2>&1 || true
    fi
    echo ">>> [$p] done; cooldown ${COOLDOWN}s"; sleep "$COOLDOWN"
done

echo ">>> render:  PROM_URL=$PROM python3 report/report_vegeta.py $RESULTS"
